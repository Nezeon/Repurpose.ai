import React from 'react';
import { motion } from 'framer-motion';
import { Sparkles, Bot, Copy, CheckCircle } from 'lucide-react';
import { cn } from '../../utils/helpers';
import { copyToClipboard } from '../../utils/helpers';
import Card from '../common/Card';
import Button from '../common/Button';
import { useState } from 'react';

const AIInsights = ({ synthesis, drugName, className }) => {
  const [copied, setCopied] = useState(false);

  const handleCopy = async () => {
    const success = await copyToClipboard(synthesis);
    if (success) {
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    }
  };

  if (!synthesis) {
    return (
      <Card className={cn('p-6', className)}>
        <div className="flex items-center justify-center gap-3 py-8 text-text-muted">
          <Bot className="w-6 h-6" />
          <span>AI synthesis not available</span>
        </div>
      </Card>
    );
  }

  // Split synthesis into paragraphs
  const paragraphs = synthesis.split('\n\n').filter(Boolean);

  return (
    <Card className={cn('p-6', className)}>
      {/* Header */}
      <div className="flex items-center justify-between mb-4">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 bg-gradient-to-br from-brand-yellow/20 to-brand-teal/20 rounded-xl flex items-center justify-center">
            <Sparkles className="w-5 h-5 text-brand-yellow" />
          </div>
          <div>
            <h3 className="font-semibold text-text-primary">AI Analysis Summary</h3>
            <p className="text-sm text-text-muted">
              Generated insights for {drugName}
            </p>
          </div>
        </div>

        <Button
          variant="ghost"
          size="sm"
          onClick={handleCopy}
          leftIcon={copied ? CheckCircle : Copy}
          className={copied ? 'text-success' : ''}
        >
          {copied ? 'Copied!' : 'Copy'}
        </Button>
      </div>

      {/* Content */}
      <motion.div
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        className="prose prose-invert prose-sm max-w-none"
      >
        {paragraphs.map((paragraph, index) => {
          // Check if it's a header (starts with #)
          if (paragraph.startsWith('#')) {
            const level = paragraph.match(/^#+/)[0].length;
            const text = paragraph.replace(/^#+\s*/, '');
            const HeadingTag = `h${Math.min(level + 2, 6)}`;
            return (
              <motion.div
                key={index}
                initial={{ opacity: 0, y: 10 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: index * 0.05 }}
              >
                <HeadingTag className="text-text-primary font-semibold mt-4 mb-2">
                  {text}
                </HeadingTag>
              </motion.div>
            );
          }

          // Check if it's a list
          if (paragraph.includes('\n- ') || paragraph.startsWith('- ')) {
            const items = paragraph.split('\n').filter(Boolean);
            return (
              <motion.ul
                key={index}
                initial={{ opacity: 0, y: 10 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: index * 0.05 }}
                className="list-disc list-inside space-y-1 text-text-secondary"
              >
                {items.map((item, i) => (
                  <li key={i}>{item.replace(/^-\s*/, '')}</li>
                ))}
              </motion.ul>
            );
          }

          // Regular paragraph
          return (
            <motion.p
              key={index}
              initial={{ opacity: 0, y: 10 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ delay: index * 0.05 }}
              className="text-text-secondary leading-relaxed mb-3"
            >
              {paragraph}
            </motion.p>
          );
        })}
      </motion.div>

      {/* Footer */}
      <div className="mt-6 pt-4 border-t border-brand-border flex items-center gap-2 text-xs text-text-muted">
        <Bot className="w-3.5 h-3.5" />
        <span>Generated by AI based on aggregated evidence from multiple sources</span>
      </div>
    </Card>
  );
};

export default AIInsights;
